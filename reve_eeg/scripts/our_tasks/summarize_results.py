from __future__ import annotations

import argparse
import csv
import html
import json
from pathlib import Path
from typing import Any


PROJECT_ROOT = Path(__file__).resolve().parents[2]
SUMMARY_COLUMNS = [
    "dataset",
    "mode",
    "seed",
    "best_stage",
    "best_epoch",
    "val_loss",
    "val_accuracy",
    "val_balanced_accuracy",
    "val_macro_f1",
    "val_weighted_f1",
    "val_cohen_kappa",
    "val_auroc",
    "val_auc_pr",
    "checkpoint_path",
    "prediction_csv_path",
    "tensorboard_path",
    "status",
    "run_dir",
]


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Summarize course-project runs under outputs/course_project.")
    parser.add_argument("--out-root", type=str, default="outputs/course_project", help="Run output root to scan.")
    parser.add_argument("--save-html", action="store_true", help="Compatibility flag; HTML is generated by default.")
    parser.add_argument("--save-csv", action="store_true", help="Compatibility flag; CSV is generated by default.")
    return parser.parse_args()


def resolve_path(path_like: str | Path) -> Path:
    path = Path(path_like)
    if path.is_absolute():
        return path
    return PROJECT_ROOT / path


def to_rel_or_abs(path: Path | None) -> str:
    if path is None:
        return ""
    try:
        return str(path.resolve().relative_to(PROJECT_ROOT.resolve()))
    except ValueError:
        return str(path)


def parse_float(value: str | None) -> float | None:
    if value is None or value == "":
        return None
    try:
        return float(value)
    except ValueError:
        return None


def best_row_key(row: dict[str, Any]) -> tuple[float, float, float]:
    return (
        float(row.get("val_balanced_accuracy") if row.get("val_balanced_accuracy") is not None else float("-inf")),
        float(row.get("val_macro_f1") if row.get("val_macro_f1") is not None else float("-inf")),
        float(row.get("val_accuracy") if row.get("val_accuracy") is not None else float("-inf")),
    )


def read_metrics_rows(metrics_path: Path) -> list[dict[str, Any]]:
    rows: list[dict[str, Any]] = []
    with metrics_path.open("r", encoding="utf-8", newline="") as handle:
        reader = csv.DictReader(handle)
        for raw_row in reader:
            rows.append(
                {
                    "stage": raw_row.get("stage", ""),
                    "epoch": raw_row.get("epoch", ""),
                    "val_loss": parse_float(raw_row.get("val_loss")),
                    "val_accuracy": parse_float(raw_row.get("val_accuracy")),
                    "val_balanced_accuracy": parse_float(raw_row.get("val_balanced_accuracy")),
                    "val_macro_f1": parse_float(raw_row.get("val_macro_f1")),
                    "val_weighted_f1": parse_float(raw_row.get("val_weighted_f1")),
                    "val_cohen_kappa": parse_float(raw_row.get("val_cohen_kappa")),
                    "val_auroc": parse_float(raw_row.get("val_auroc")),
                    "val_auc_pr": parse_float(raw_row.get("val_auc_pr")),
                },
            )
    return rows


def scan_run_dirs(out_root: Path) -> list[Path]:
    run_dirs: list[Path] = []
    for dataset_dir in sorted(out_root.iterdir()) if out_root.exists() else []:
        if not dataset_dir.is_dir():
            continue
        for mode_dir in sorted(dataset_dir.iterdir()):
            if not mode_dir.is_dir():
                continue
            for seed_dir in sorted(mode_dir.iterdir()):
                if seed_dir.is_dir() and seed_dir.name.startswith("seed"):
                    run_dirs.append(seed_dir)
    return run_dirs


def summarize_one_run(run_dir: Path) -> dict[str, Any]:
    dataset = run_dir.parent.parent.name
    mode = run_dir.parent.name
    seed = run_dir.name.replace("seed", "", 1)

    metrics_path = run_dir / "metrics.csv"
    checkpoint_path = run_dir / "model_best.pth"
    prediction_csv_path = run_dir / "test_predictions.csv"
    tensorboard_path = run_dir / "tensorboard"

    status_parts: list[str] = []
    best_metrics: dict[str, Any] | None = None
    if not metrics_path.exists():
        status_parts.append("missing_metrics_csv")
    else:
        try:
            metric_rows = read_metrics_rows(metrics_path)
            valid_rows = [row for row in metric_rows if row.get("val_balanced_accuracy") is not None]
            if valid_rows:
                best_metrics = max(valid_rows, key=best_row_key)
            else:
                status_parts.append("metrics_without_val_scores")
        except Exception as exc:  # pragma: no cover - defensive summary path
            status_parts.append(f"metrics_parse_error:{type(exc).__name__}")

    if not checkpoint_path.exists():
        status_parts.append("missing_checkpoint")
    if not prediction_csv_path.exists():
        status_parts.append("prediction_missing")
    if not tensorboard_path.exists():
        status_parts.append("tensorboard_missing")
    if not status_parts:
        status_parts.append("ok")

    summary = {
        "dataset": dataset,
        "mode": mode,
        "seed": seed,
        "best_stage": best_metrics.get("stage", "") if best_metrics else "",
        "best_epoch": best_metrics.get("epoch", "") if best_metrics else "",
        "val_loss": best_metrics.get("val_loss") if best_metrics else None,
        "val_accuracy": best_metrics.get("val_accuracy") if best_metrics else None,
        "val_balanced_accuracy": best_metrics.get("val_balanced_accuracy") if best_metrics else None,
        "val_macro_f1": best_metrics.get("val_macro_f1") if best_metrics else None,
        "val_weighted_f1": best_metrics.get("val_weighted_f1") if best_metrics else None,
        "val_cohen_kappa": best_metrics.get("val_cohen_kappa") if best_metrics else None,
        "val_auroc": best_metrics.get("val_auroc") if best_metrics else None,
        "val_auc_pr": best_metrics.get("val_auc_pr") if best_metrics else None,
        "checkpoint_path": to_rel_or_abs(checkpoint_path if checkpoint_path.exists() else None),
        "prediction_csv_path": to_rel_or_abs(prediction_csv_path if prediction_csv_path.exists() else None),
        "tensorboard_path": to_rel_or_abs(tensorboard_path if tensorboard_path.exists() else None),
        "status": ";".join(status_parts),
        "run_dir": to_rel_or_abs(run_dir),
    }
    return summary


def write_csv(path: Path, rows: list[dict[str, Any]]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8", newline="") as handle:
        writer = csv.DictWriter(handle, fieldnames=SUMMARY_COLUMNS)
        writer.writeheader()
        for row in rows:
            writer.writerow({column: row.get(column, "") for column in SUMMARY_COLUMNS})


def choose_best_by_dataset(rows: list[dict[str, Any]]) -> list[dict[str, Any]]:
    grouped: dict[str, list[dict[str, Any]]] = {}
    for row in rows:
        grouped.setdefault(str(row["dataset"]), []).append(row)

    best_rows: list[dict[str, Any]] = []
    for dataset, dataset_rows in sorted(grouped.items()):
        valid_rows = [row for row in dataset_rows if row.get("val_balanced_accuracy") is not None]
        if valid_rows:
            best_rows.append(max(valid_rows, key=best_row_key))
        else:
            best_rows.append(dataset_rows[0])
    return best_rows


def build_html_table(rows: list[dict[str, Any]]) -> str:
    headers = SUMMARY_COLUMNS
    header_html = "".join(f"<th>{html.escape(column)}</th>" for column in headers)
    body_rows: list[str] = []
    for row in rows:
        cells = []
        for column in headers:
            value = row.get(column, "")
            if value is None:
                value = ""
            cells.append(f"<td>{html.escape(str(value))}</td>")
        body_rows.append("<tr>" + "".join(cells) + "</tr>")
    body_html = "\n".join(body_rows)
    return f"""<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="utf-8">
  <title>Course Project Best Runs</title>
  <style>
    body {{ font-family: Arial, sans-serif; margin: 24px; }}
    table {{ border-collapse: collapse; width: 100%; }}
    th, td {{ border: 1px solid #d0d7de; padding: 8px 10px; text-align: left; }}
    th {{ background: #f6f8fa; position: sticky; top: 0; }}
    tr:nth-child(even) {{ background: #fbfbfc; }}
    code {{ font-family: Consolas, monospace; }}
  </style>
</head>
<body>
  <h1>Best Course-Project Runs By Dataset</h1>
  <table>
    <thead><tr>{header_html}</tr></thead>
    <tbody>
      {body_html}
    </tbody>
  </table>
</body>
</html>
"""


def main() -> None:
    args = parse_args()
    out_root = resolve_path(args.out_root)
    run_dirs = scan_run_dirs(out_root)
    summaries = [summarize_one_run(run_dir) for run_dir in run_dirs]
    best_rows = choose_best_by_dataset(summaries) if summaries else []

    summary_all_path = out_root / "summary_all_runs.csv"
    summary_best_csv_path = out_root / "summary_best_by_dataset.csv"
    summary_best_html_path = out_root / "summary_best_by_dataset.html"

    write_csv(summary_all_path, summaries)
    write_csv(summary_best_csv_path, best_rows)
    summary_best_html_path.write_text(build_html_table(best_rows), encoding="utf-8")

    print(f"Scanned {len(run_dirs)} run directories under {out_root}")
    print(f"Saved: {summary_all_path}")
    print(f"Saved: {summary_best_csv_path}")
    print(f"Saved: {summary_best_html_path}")


if __name__ == "__main__":
    main()
